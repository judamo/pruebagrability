    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int t = Integer.parseInt(cttest.getText());//nro de pruebas
        n = Integer.parseInt(ctn.getText());//tamaÃ±o de la matriz
        m = Integer.parseInt(ctm.getText());//numero de operaciones

        boolean ban = validar(t, n, m);

        if (ban) {
            m3d = controlador.iniciarMatriz(n);
            updateOperaciones(m);
            imprimirMatriz();
        } else {
            JOptionPane.showMessageDialog(null, "No se puede completar Operacion");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (m > 0) {

            int value   = Integer.parseInt(uvalue.getText());
            int x       = Integer.parseInt(ux.getText());
            int y       = Integer.parseInt(uy.getText());
            int z       = Integer.parseInt(uz.getText());

            boolean ban = validarUpdate(value, x, y, z);

            if (ban) {
                controlador.updateMatriz(x, y, z, value);
                m--;
                updateOperaciones(m);
                imprimirMatriz();
            } else {
                JOptionPane.showMessageDialog(null, "No cumple las validaciones");
            }

        } else {
            JOptionPane.showMessageDialog(null, "No tiene mas Operaciones");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (m > 0) {

            int x0 = Integer.parseInt(xini.getText());
            int y0 = Integer.parseInt(yini.getText());
            int z0 = Integer.parseInt(zini.getText());

            int xn = Integer.parseInt(xfi.getText());
            int yn = Integer.parseInt(yfi.getText());
            int zn = Integer.parseInt(zfi.getText());

            boolean ban = validacionquery(x0, y0, z0, xn, yn, zn);

            if (ban) {
                int suma = controlador.query(x0, y0, z0, xn, yn, zn);
                lblsuma.setText("Suma : " + suma);
                m--;
                updateOperaciones(m);
            } else {
                JOptionPane.showMessageDialog(null, "No cumple validaciones");
            }

        } else {
            JOptionPane.showMessageDialog(null, "No tiene mas Operaciones");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

