    private void updateOperaciones(int m) {
        lbloperaciones.setText("Nro " + m);
    }

    private boolean validar(int t, int n, int m) {
        if (t < 1 || t >= 50) {
            return false;
        } else if (n < 1 || n >= 100) {
            return false;
        } else if (m < 1 || m >= 1000) {
            return false;
        }
        return true;
    }

    private boolean validarUpdate(int value, int x, int y, int z) {
        if (value <= -1000000000 || value >= 1000000000) {
            return false;
        } else if (x < 1 || x > n) {
            return false;
        } else if (y < 1 || y > n) {
            return false;
        } else if (z < 1 || z > n) {
            return false;
        }

        return true;
    }

    private boolean validacionquery(int x0, int y0, int z0, int xn, int yn, int zn) {

        if (x0 < 1 || y0 < 1 || z0 < 1 || xn < 1 || yn < 1 || zn < 1) {
            return false;
        } else if (x0 > n || y0 > n || z0 > n || xn > n || yn > n || zn > n) {
            return false;
        } else if (xn < x0 || yn < y0 || zn < z0) {
            return false;
        }

        return true;
    }
